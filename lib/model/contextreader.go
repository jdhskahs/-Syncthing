package model

import (
	"context"
	"io"
)

// A contextReader wraps a Reader and adds in a context.Done check before
// every Read.
type contextReader struct {
	ctx context.Context
	r   io.Reader
}

func (r *contextReader) Read(p []byte) (int, error) {
	select {
	case <-r.ctx.Done():
		return 0, r.ctx.Err()
	default:
	}
	return r.r.Read(p)
}
